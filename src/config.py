"""Configuration management — read/write .env files for API keys."""
from __future__ import annotations

import os
from pathlib import Path

from .providers import PROVIDERS, ProviderConfig

ENV_FILE = Path.home() / '.claude-code-robin' / '.env'


def load_env(path: Path | None = None) -> dict[str, str]:
    """Load key=value pairs from a .env file into a dict (and os.environ)."""
    path = path or ENV_FILE
    env: dict[str, str] = {}
    if not path.exists():
        return env
    for line in path.read_text(encoding='utf-8').splitlines():
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        if '=' not in line:
            continue
        key, _, value = line.partition('=')
        key = key.strip()
        value = value.strip().strip('"').strip("'")
        if value:
            env[key] = value
            os.environ.setdefault(key, value)
    return env


def save_env(env: dict[str, str], path: Path | None = None) -> Path:
    """Write key=value pairs to the .env file."""
    path = path or ENV_FILE
    path.parent.mkdir(parents=True, exist_ok=True)

    lines = ['# claude-code-robin API keys', '# Generated by `claude-code-robin configure`', '']
    for provider in PROVIDERS:
        key = provider.env_key
        val = env.get(key, '')
        if val:
            lines.append(f'# {provider.label_zh}')
            lines.append(f'{key}={val}')
            lines.append('')
    # Also preserve DEFAULT_PROVIDER if set
    if 'DEFAULT_PROVIDER' in env:
        lines.append(f'DEFAULT_PROVIDER={env["DEFAULT_PROVIDER"]}')
        lines.append('')

    path.write_text('\n'.join(lines), encoding='utf-8')
    return path


def configure_interactive() -> None:
    """Interactive configuration wizard — prompt user for each provider's API key."""
    print('=== claude-code-robin 模型配置向导 ===')
    print('Configure API keys for LLM providers.')
    print('Press Enter to skip a provider.\n')

    # Load existing config
    existing = load_env()
    env: dict[str, str] = {}

    for p in PROVIDERS:
        current = existing.get(p.env_key) or os.environ.get(p.env_key, '')
        masked = f' (current: ...{current[-6:]})' if current else ''
        try:
            val = input(f'  {p.label_zh} [{p.env_key}]{masked}: ').strip()
        except (EOFError, KeyboardInterrupt):
            print('\n\nConfiguration cancelled.')
            return
        env[p.env_key] = val if val else current

    # Choose default provider
    configured = [p for p in PROVIDERS if env.get(p.env_key)]
    if configured:
        print('\n--- 选择默认模型 / Choose default provider ---')
        for i, p in enumerate(configured, 1):
            print(f'  {i}. {p.label_zh} ({p.name}) [default model: {p.default_model}]')
        try:
            choice = input(f'\nDefault provider [1]: ').strip()
        except (EOFError, KeyboardInterrupt):
            print()
            choice = ''
        idx = int(choice) - 1 if choice.isdigit() and 1 <= int(choice) <= len(configured) else 0
        env['DEFAULT_PROVIDER'] = configured[idx].name
        print(f'\n  Default: {configured[idx].label_zh}')

    path = save_env(env)
    print(f'\nConfiguration saved to {path}')
    print('You can now run `claude-code-robin interactive` to start chatting!')


def get_default_provider_name() -> str | None:
    """Return the default provider name from config or env."""
    return os.environ.get('DEFAULT_PROVIDER')
